package com.nibble.starfood.webservices.model;

public class FeedBackSend {
	private int qutId;
	private int ansId;
	private int cmt;
	public int getQutId() {
		return qutId;
	}
	public void setQutId(int qutId) {
		this.qutId = qutId;
	}
	public int getAnsId() {
		return ansId;
	}
	public void setAnsId(int ansId) {
		this.ansId = ansId;
	}
	public int getCmt() {
		return cmt;
	}
	public void setCmt(int cmt) {
		this.cmt = cmt;
	}
	
	

}
