package com.nibble.starfood.webservices.model;

public class MenuList {

	

	int foodType;
	int mealTime;
	int pkgDur;
	int menuType;
	int weekDur;
	int start;
	int end;
	
	
	
	public int getFoodType() {
		return foodType;
	}

	public void setFoodType(int foodType) {
		this.foodType = foodType;
	}

	public int getMealTime() {
		return mealTime;
	}

	public void setMealTime(int mealTime) {
		this.mealTime = mealTime;
	}
	public int getPkgDur() {
		return pkgDur;
	}

	public void setPkgDur(int pkgDur) {
		this.pkgDur = pkgDur;
	}

	public int getMenuType() {
		return menuType;
	}

	public void setMenuType(int menuType) {
		this.menuType = menuType;
	}

	public int getWeekDur() {
		return weekDur;
	}

	public void setWeekDur(int weekDur) {
		this.weekDur = weekDur;
	}
	
	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}
	


}
